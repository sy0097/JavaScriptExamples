console.log(typeof a)


//Declaring a number
let b=10
console.log(b);
console.log(typeof b + b);
/* here everything treated as number like float decimal negative number*/


//Declaring a Boolean
let status=false
console.log(typeof status)
console.log(status);

//declaring a undefined 
//Whenever there is an variable but you are not defining a value then it will declare as undefined
let fname
console.log(fname);
console.log(typeof fname);


//JS is dynamic typing language

//declaring a null

let c=null
